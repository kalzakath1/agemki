/**
 * Genera los píxeles de una fuente escalando los datos 8×8.
 * @param {number} gw  ancho de glifo destino (8 o 16)
 * @param {number} gh  alto de glifo destino (8 o 16)
 * @returns {Uint8Array} píxeles de índice, tamaño (gw*FONT_N_GLYPHS × gh)
 */
function _buildFontPixels(gw, gh) {
  const totalW = gw * FONT_N_GLYPHS
  const pixels = new Uint8Array(totalW * gh)
  const sx = gw / 8
  const sy = gh / 8

  for (let gi = 0; gi < FONT_N_GLYPHS; gi++) {
    const base = gi * 8
    const gx0  = gi * gw
    for (let srcRow = 0; srcRow < 8; srcRow++) {
      const bits = GLYPHS_8x8[base + srcRow] || 0
      for (let dy = 0; dy < sy; dy++) {
        const py = srcRow * sy + dy
        for (let srcCol = 0; srcCol < 8; srcCol++) {
          const bit = (bits >> (7 - srcCol)) & 1
          for (let dx = 0; dx < sx; dx++) {
            const px = gx0 + srcCol * sx + dx
            pixels[py * totalW + px] = bit ? 1 : 0
          }
        }
      }
    }
  }
  return pixels
}

/**
 * Genera los PCX de fuentes base en el directorio de fonts del proyecto.
 * Solo escribe si el fichero no existe.
 *
 * @param {string}   fontsDir  Ruta a assets/fonts/ del proyecto
 * @param {Function} log       Función de log (text, type?) — opcional
 */
export function ensureBaseFonts(fontsDir, log) {
  const specs = [
    { name: 'small',  gw: 8,  gh: 8  },
    { name: 'medium', gw: 8,  gh: 16 },
    { name: 'large',  gw: 16, gh: 16 },
  ]

  if (!existsSync(fontsDir)) {
    try { mkdirSync(fontsDir, { recursive: true }) } catch { /* ignore */ }
  }

  for (const s of specs) {
    const fpath = join(fontsDir, `${s.name}.PCX`)
    // Siempre regenerar fuentes base para reflejar cambios en el charset
    try {
      const pixels = _buildFontPixels(s.gw, s.gh)
      const totalW = s.gw * FONT_N_GLYPHS
      const pcx    = _encodePCX(pixels, totalW, s.gh)
      writeFileSync(fpath, pcx)
      if (log) log(`  Fuente base generada: ${s.name}.PCX (${totalW}x${s.gh})`, 'info')
    } catch (e) {
      if (log) log(`  Aviso: no se pudo generar fuente ${s.name}.PCX — ${e.message}`, 'warn')
    }
  }
}

