/**
 * fontGenerator.js — Generador de fuentes bitmap PCX para el motor DOS
 *
 * Genera 3 PCX de fuentes base (fnt_small 8×8, fnt_medium 8×16, fnt_large 16×16)
 * en JS puro, sin Canvas ni DOM.
 *
 * Formato del PCX de fuente:
 *   - 1 fila de FONT_N_GLYPHS (112) glifos
 *   - Índice 0 = transparente, índice 1 = color key (blanco)
 *   - Charset: ASCII 32-127 (pos 0-95) + españoles cp850 (pos 96-111)
 *
 * Se llama desde el build antes de generar FONTS.DAT.
 * Los PCX se escriben en assets/fonts/ del proyecto
 * SOLO si no existen (el usuario puede sustituirlos con su propia fuente).
 */

import { writeFileSync, existsSync, mkdirSync } from 'fs'
import { join } from 'path'

const FONT_N_GLYPHS = 144  /* ASCII 32-127 (96) + españoles 96-111 (16) + catalán/francés 112-143 (32) */

// ── Paleta PCX (256 colores) ─────────────────────────────────────────────────
function _buildPalette() {
  const pal = Buffer.alloc(768, 0)
  pal[0] = 255; pal[1] = 0;   pal[2] = 255  // índice 0: magenta (transparente)
  pal[3] = 255; pal[4] = 255; pal[5] = 255  // índice 1: blanco (color key)
  return pal
}

// ── Encoder PCX RLE ──────────────────────────────────────────────────────────
function _encodePCX(pixels, width, height) {
  const hdr = Buffer.alloc(128, 0)
  hdr[0] = 0x0A; hdr[1] = 0x05; hdr[2] = 0x01; hdr[3] = 0x08
  hdr.writeUInt16LE(0, 4); hdr.writeUInt16LE(0, 6)
  hdr.writeUInt16LE(width - 1, 8); hdr.writeUInt16LE(height - 1, 10)
  hdr.writeUInt16LE(72, 12); hdr.writeUInt16LE(72, 14)
  hdr[65] = 1
  hdr.writeUInt16LE(width, 66)
  hdr.writeUInt16LE(1, 68)

  const rleRows = []
  let totalRleBytes = 0
  for (let y = 0; y < height; y++) {
    const row = []
    let x = 0
    while (x < width) {
      const val = pixels[y * width + x]
      let cnt = 1
      while (cnt < 63 && x + cnt < width && pixels[y * width + x + cnt] === val) cnt++
      if (cnt > 1 || val >= 0xC0) { row.push(0xC0 | cnt, val) }
      else { row.push(val) }
      x += cnt
    }
    rleRows.push(row)
    totalRleBytes += row.length
  }

  const out = Buffer.alloc(128 + totalRleBytes + 1 + 768)
  hdr.copy(out, 0)
  let pos = 128
  for (const row of rleRows) for (const b of row) out[pos++] = b
  out[pos++] = 0x0C
  _buildPalette().copy(out, pos)
  return out
}

